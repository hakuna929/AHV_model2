function [CT] = thrust_coeffs(Ma, alpha, beta, dT)
% alpha, beta使用度要统一
adeg = alpha*180/pi;
bdeg = beta*180/pi;

%  C_Tc = C_Tc(Ma,α,β,δ_T)  
CTc =  0.1029      * dT ...
     + 0.02022     * Ma*cosd(bdeg) * dT ...
     - 0.001757    * adeg  * dT ...
     + 0.000088233 * adeg.^2 * dT ...
     + 0.001221    * (Ma*cosd(bdeg)).^2 .* dT;

%  C_Tn = C_Tn(Ma,α,β,δ_T)  
CTn =  0.03791 ...
     + 0.005176   * adeg ...
     - 0.01235    * Ma*cosd(bdeg) ...
     - 0.00054887 * (Ma*cosd(bdeg))* adeg ...
     + 0.00096897 * (Ma*cosd(bdeg)).^2 ...
     + 0.05627    * dT ...
     - 0.00077467 * (adeg * dT) ...
     + 0.002103   * (Ma * cosd(bdeg) * dT);
CT = CTc + CTn;

% % m_f/delta_T = m_f(Ma, alpha, beta, H, delta_T)/delta_T  
% mdot_f_over_dT =  2.4805 ...
%                 - 0.05455  * adeg ...
%                 + 0.001599 * adeg.^2 ...
%                 - 0.204    * H ...
%                 + 0.486    * Ma*cosd(bdeg) ...
%                 + 0.002515 * adeg .* H ...
%                 + 0.003635 * H.^2 ...
%                 - 0.008598 * Ma*cosd(bdeg)* adeg ...
%                 - 0.01216  * Ma*cosd(bdeg)* H;
% 
% mdot_f = mdot_f_over_dT * dT;

end